import React, { createContext, useContext } from "react";

import { zero } from "../helpers/I64";
import { useMemoInt64 } from "../hooks/Int64Hooks";

const defaultThreadContext = { threadKey: zero };
const ThreadContext = createContext(defaultThreadContext);

export function useMWPAudioPlaybackThreadKey() {
  return useContext(ThreadContext).threadKey;
}

export const MWPAudioPlaybackThreadContextProvider = ({
  children,
  threadKey,
}) => {
  const contextValue = useMemoInt64(() => ({ threadKey }), [threadKey]);
  return (
    <ThreadContext.Provider value={contextValue}>
      {children}
    </ThreadContext.Provider>
  );
};

MWPAudioPlaybackThreadContextProvider.displayName =
  MWPAudioPlaybackThreadContextProvider.name;
